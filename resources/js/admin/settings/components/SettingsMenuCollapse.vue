  >
    <div class="menu-width">
      <p class="menu-title"> {{ $t('Settings') }} </p>
      <div
        v-for="(menu, index) in menuGroups"
        :key="menu.menu_group"
      >
        <div 
          :id="`headingOne${menu.menu_group_order}`"
          @click="updateCollapse(menu)"
        >
          <h5 class="mb-0">
            <div
              class="btn menu-header"
              data-toggle="collapse"
              :data-target="`#collapseOne${menu.id}`"
              aria-expanded="false"
              :aria-controls="`collapseOne${menu.id}`"
            >
              <span>
                <i :class="`fas fa-${menu.ui.icon}`"/>
                {{ menu.menu_group }}
              </span>
              <i :class="{ 'fas fa-caret-down': collapsedMenus[menu.id], 'fas fa-caret-up': !collapsedMenus[menu.id] }"/>
          </div>
          </h5>
        </div>
  
        <div
          :id="`collapseOne${menu.id}`"
          class="collapse"
          :class="menu.menu_group_order === 1 ? 'show' : ''"
          :aria-labelledby="`headingOne${menu.menu_group_order}`"
        >
          <b-list-group>
            <b-list-group-item
              v-for="item in menu.groups"
              :key="item.id"
              ref="processItems"
              :class="{ 'list-item-selected': isSelected(item.id) }"
              class="list-item"
              @click="selectItem(item)"
            >
              {{ item.name }}
            </b-list-group-item>
          </b-list-group>
        </div>
      </div>
    </div>
  </div>
</template>

<script>
export default {
  props: [
    "selectGroup",
  ],
  data() {
    return {
      currentTab: 0,
      groups: [],
      menuGroups: [],
      selectedItem: "Action by Email",
      collapsedMenus: {},
      harcodedResponse: [
        {
          id: '4',
          ui: {icon: "users"},
          menu_group_order: 4,
          menu_group: "User Settings",
          groups:[
              {
                  id: "settign3",
                  name: "settingx3e"
              
              },
          ]
        },
        {
          id: '2',
          ui: {icon: "puzzle-piece"},
          menu_group_order: 2,
          menu_group: "Integrations",
          groups:[
              {
                  id: "settign1rr",
                  name: "settingxd"
              
              },
              {
                  id: "settign2k",
                  name: "settingx2"
              
              },
          ]
        },
        {
          id: '1',
          ui: {icon: "envelope-open-text"},
          menu_group_order: 1,
          menu_group: "Email",
          groups:[
              {
                  id: "Action by Email",
                  name: "Action by Email"
              
              },
              {
                  id: "Email Default Settings",
                  name: "Email Default Settings"
              
              },
          ]
        },
        {
          id: '3',
          ui: {icon: "sign-in-alt"},
          menu_group_order: 3,
          menu_group: "Log-In & Auth",
          groups:[
              {
                  id: "settign2qq",
                  name: "settingx2"
              
              },
          ]
        },
      ]
    };
  },
  mounted() {
    this.getMenuGrups();
  },
  methods: {
    getMenuGrups() {
      ProcessMaker.apiClient.get('/settings/menu-groups?order_by=menu_group_order')
      .then(response => {
        this.transformResponse(response.data);
      });
    },
    transformResponse(response) {
      var arrayCollaped = {};
      response.sort((a, b) => {
          return a.menu_group_order - b.menu_group_order;
      });
      response.forEach((element) => {
        arrayCollaped[element.id] = !(element.menu_group_order === 1);
      });
      this.collapsedMenus = arrayCollaped
      this.menuGroups = response;
    },
    isSelected(item) {
      return this.selectedItem === item;
    },
    selectItem(item) {
      this.selectedItem = item.id;
      this.$emit("selectGroup", item);
    },
    updateCollapse(menu) {
      this.collapsedMenus[menu.id] = !this.collapsedMenus[menu.id];
    }
  },
};
</script>

